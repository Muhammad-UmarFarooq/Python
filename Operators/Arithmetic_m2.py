a=int(input("Enter a value:"))
b=int(input("Enter b value:"))
# Addition
print('Sum: ', a+b)

# Subtraction
print ('Subtraction: ', a - b)   

# Multiplication
print ('Multiplication: ', a * b)  

# Division
print ('Division:', a / b) 

# Floor division
print('Floor division: ', a//b)

# Modulo
print('Modulo: ', a%b)

# a to the power b
print('Power: ', a ** b)
